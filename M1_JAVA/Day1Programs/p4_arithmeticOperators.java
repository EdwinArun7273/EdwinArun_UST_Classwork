
public class p4_arithmeticOperators {

	public static void main(String[] args) {
		System.out.println("5 + 5 = " + (5+5));
		System.out.println("5 - 5 = " + (5-5));
		System.out.println("5 * 5 = " + (5*5));
		System.out.println("5 / 5 = " + (5/5));
		System.out.println("10 % 3 = " + (10%3));
	}

}
